// Incremento e decremento de Variáveis
// todas as operações matemáticas podem ser feitas

var num = 10;

// incremento 
num = num + 1; // ou num++;

// para incrementos maiores que 1
num = num + 2; //ou num += 2

// decremento 
num = num -1; // ou num--;

// para decremento maiores que 1
num = num - 2 // ou num -= 2

//pós incremento num++
document.write(num++);  

document.write("<br>")

// pré incremento ++num
document.write(++num) 

document.write("<br>")

// inverter valor
num = num*-1; // ou num *=- 1 ou -num

